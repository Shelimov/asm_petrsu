.include "macros.S"	# подключение файла с макроопределениями

.text
        
.global _start
_start:	
    movl $5, %eax # счетчик цикла
	movl $1, %ebx # какое-то число результат программы

L1: 	
    cmpl  $0, %eax # условие цикла. Сравниваем счетчик с нулем
	je    L2 # если счетчик равен 0, то завершаем программу
			 # в противном случае продолжаем выполнение
	imull %eax, %ebx # умножаем число результат на счетчик
					 # и записываем в это же число результат
	decl  %eax # декремент счетчика цикла
	jmp   L1 # переходим к началу цикла для проверки условия
	
L2:	
        Exit # завершаем программу
.end
